    private void btnChonAnhActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChonAnhActionPerformed
       JFileChooser chooser = new JFileChooser("Downloads"); // đường dẫn đến thư mục ảnh của nhân viên
        chooser.showOpenDialog(this);
        File f = chooser.getSelectedFile();
        filename = f.getAbsolutePath();
        ImageIcon imageicon = new ImageIcon(new ImageIcon(filename).getImage().getScaledInstance(txtanh.getWidth(), txtanh.getHeight(), Image.SCALE_SMOOTH));
        txtanh.setIcon(imageicon);
        // Chuyển ảnh thành dữ liệu byte để lưu và sql
        try {
            File image = new File(filename);
            FileInputStream fis = new FileInputStream(image);
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            byte[] buf = new byte[1024];
            for(int i; (i=fis.read(buf))!= -1;){
                bos.write(buf, 0, i);
            }
            img_NV = bos.toByteArray();
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnChonAnhActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        int row = table.getSelectedRow();
        if (row >= 0) {
            String ma = (String) table.getValueAt(row, 0);
            for (NhanVien nv : listNV) {
                if (nv.getMsnv().equals(ma)) {
                    txtnmsnv.setText(nv.getMsnv());
                    txtdc.setText(nv.getDiachi());
                    txtnhtnv.setText(nv.getHoten());
                    txtemail.setText(nv.getEmail());
                    txtsdt.setText(nv.getSdt().trim());
                    if (nv.getGioitinh() == 1) {
                        rdn.setSelected(true);
                    } else {
                        rdnu.setSelected(true);
                    }
                    txtns.setDate(nv.getNgaysinh());
                    cbpb.setSelectedItem(nv.getPhongban());
                    cbcv.setSelectedItem(nv.getChucvu());
                    txtnl.setText(nv.getLuong().trim());
               
                    try {
                        byte[] img = nv.getImg();
                        ImageIcon imageicon = new ImageIcon(new ImageIcon(img).getImage().getScaledInstance(txtanh.getWidth(), txtanh.getHeight(), Image.SCALE_SMOOTH));
                        txtanh.setIcon(imageicon);  
                        txtcanhbao.setVisible(false);
                    } catch (Exception e) {
                        
                       ImageIcon img_default = new ImageIcon("C:\\Users\\Acer\\Downloads\\anh\\hung.jpg");// ảnh mặc định
                       txtanh.setIcon(img_default);
                       txtcanhbao.setVisible(true);
                    }
                    
                    return;
                }
            }
        }
    }//GEN-LAST:event_tableMouseClicked

