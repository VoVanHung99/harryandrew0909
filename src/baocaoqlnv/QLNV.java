    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        if (txtnmsnv.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Mã nhân viên không được rỗng !","Cảnh Báo", JOptionPane.WARNING_MESSAGE);
            txtnmsnv.setBackground(Color.yellow);
            txtnmsnv.requestFocus();
            return;
        } else {
            txtnmsnv.setBackground(Color.white);
        }
        if (txtsdt.getText().length() > 10) {
            JOptionPane.showMessageDialog(this, "SĐT không vượt 10 ký tự !","Cảnh Báo", JOptionPane.WARNING_MESSAGE);
            txtsdt.setBackground(Color.yellow);
            txtsdt.requestFocus();
            return;
        }
        try {
            int sdt = Integer.parseInt(txtsdt.getText());
            txtsdt.setBackground(Color.white);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "SĐT phải là số !","Cảnh Báo", JOptionPane.WARNING_MESSAGE);
            txtsdt.setBackground(Color.yellow);
            txtsdt.requestFocus();
            return;
        }

        try {
            NhanVien nv = new NhanVien();
            nv.setMsnv(txtnmsnv.getText());
            nv.setHoten(txtnhtnv.getText());
            nv.setChucvu(cbcv.getSelectedItem().toString());
            nv.setDiachi(txtdc.getText());
            nv.setEmail(txtemail.getText());
            nv.setLuong(txtnl.getText());
            nv.setGioitinh(rdn.isSelected() ? 1 : 0);
            nv.setPhongban(cbpb.getSelectedItem().toString());
            nv.setSdt(txtsdt.getText());
            java.util.Date chua = txtns.getDate();
            java.sql.Date sqlDate = new java.sql.Date(chua.getTime());
            nv.setNgaysinh(sqlDate);
            nv.setImg(img_NV);
            Model m = new Model();
            m.add(nv);
            JOptionPane.showMessageDialog(this, "Add seccesfull");
            dataTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Add fail" + e.getMessage());
        }
    }//GEN-LAST:event_btnthemActionPerformed

